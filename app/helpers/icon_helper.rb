  # Render a Heroicon SVG
  # @param name [String] Icon name (e.g., "sparkles", "clock")
  # @param options [Hash] HTML options, especially :class
  # @return [String] SVG HTML
  def heroicon(name, **options)
    icon_path = HEROICONS[name.to_s]
    return fallback_icon(name, options) unless icon_path

    css_class = options[:class] || "w-6 h-6"

    content_tag(:svg, class: css_class, fill: "none", stroke: "currentColor",
                      viewBox: "0 0 24 24", "stroke-width": "1.5",
                      xmlns: "http://www.w3.org/2000/svg") do
      icon_path.html_safe
    end
  end

  private

  def fallback_icon(name, options)
    css_class = options[:class] || "w-6 h-6"
    # Return empty SVG as fallback
    content_tag(:svg, class: css_class, viewBox: "0 0 24 24") do
      content_tag(:title, name)
    end
  end
